def get_weather(iata: str) -> dict | None:
    coords = AIRPORT_COORDS.get(iata.upper())
    if not coords:
        return None
    lat, lon = coords
    url = "https://api.open-meteo.com/v1/forecast"
    params = {
        "latitude": lat, "longitude": lon,
        "current": "temperature_2m,wind_speed_10m,precipitation,cloud_cover",
        "timezone": "auto",
    }
    try:
        r = requests.get(url, params=params, timeout=5)
        if r.status_code == 200:
            c = r.json().get("current", {})
            return {
                "temp":        round(c.get("temperature_2m", 0), 1),
                "wind_speed":  round(c.get("wind_speed_10m", 0), 1),
                "precip":      round(c.get("precipitation", 0), 1),
                "cloud_cover": round(c.get("cloud_cover", 0) / 100 * 8, 1),
            }
    except Exception:
        pass
    return None
